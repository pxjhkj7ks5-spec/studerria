export function getUnitDefinition(kind: UnitKind): UnitDefinition {
  const definition = unitDefinitions.find((unit) => unit.kind === kind);
  if (!definition) {
    throw new Error(`Unknown Shieldline unit kind: ${kind}`);
  }
  return definition;
}
