import type { ThreatKind, UnitDefinition, UnitKind } from "../types/game";

const none: Record<ThreatKind, number> = {
  drone: 0,
  cruise: 0,
  ballistic: 0,
  decoy: 0,
  combined: 0,
  saturation: 0,
  geran2: 0,
  gerbera: 0,
  parodiya: 0,
  kh101: 0,
  kalibr: 0,
  iskander: 0,
};

function chances(values: Partial<Record<ThreatKind, number>>): Record<ThreatKind, number> {
  const drone = values.drone || 0;
  const cruise = values.cruise || 0;
  const ballistic = values.ballistic || 0;
  const decoy = values.decoy || 0;
  return {
    ...none,
    combined: cruise,
    saturation: drone,
    geran2: drone,
    gerbera: Math.min(98, drone + (drone ? 5 : 0)),
    parodiya: decoy || drone,
    kh101: cruise,
    kalibr: cruise,
    iskander: ballistic,
    ...values,
  };
}

